const header = document.querySelector(".site-header");
const menuButton = document.querySelector(".menu-button");
const nav = document.querySelector(".site-nav");
const dialog = document.querySelector("#case-dialog");
const dialogClose = document.querySelector(".dialog-close");
const dialogKicker = document.querySelector("#dialog-kicker");
const dialogTitle = document.querySelector("#dialog-title");
const dialogIntro = document.querySelector("#dialog-intro");
const dialogDetails = document.querySelector("#dialog-details");
const dialogLinks = document.querySelector("#dialog-links");
const dialogGallery = document.querySelector("#dialog-gallery");

document.querySelector("#year").textContent = new Date().getFullYear();

window.addEventListener("scroll", () => {
  header.classList.toggle("scrolled", window.scrollY > 20);
}, { passive: true });

menuButton.addEventListener("click", () => {
  const expanded = menuButton.getAttribute("aria-expanded") === "true";
  menuButton.setAttribute("aria-expanded", String(!expanded));
  nav.classList.toggle("open", !expanded);
});

nav.querySelectorAll("a").forEach((link) => {
  link.addEventListener("click", () => {
    menuButton.setAttribute("aria-expanded", "false");
    nav.classList.remove("open");
  });
});

const openCase = (caseId) => {
  const item = cases[caseId];
  if (!item) return;

  dialogKicker.textContent = item.kicker;
  dialogTitle.textContent = item.title;
  dialogIntro.textContent = item.intro;

  dialogDetails.innerHTML = item.details.map(([title, copy]) => `
    <section class="dialog-detail">
      <h3>${title}</h3>
      <p>${copy}</p>
    </section>
  `).join("");

  dialogLinks.innerHTML = (item.links || []).map(([label, href]) => `
    <a href="${href}" target="_blank" rel="noopener noreferrer">${label} <span aria-hidden="true">↗</span></a>
  `).join("");

  dialogGallery.innerHTML = item.images.map(([src, alt]) => `
    <figure><img src="${src}" alt="${alt}" loading="lazy"></figure>
  `).join("");

  dialog.showModal();
  document.body.classList.add("dialog-open");
  dialog.querySelector(".dialog-shell").scrollTop = 0;
};

document.querySelectorAll("[data-case]").forEach((button) => {
  button.addEventListener("click", () => openCase(button.dataset.case));
});

const closeDialog = () => {
  dialog.close();
  document.body.classList.remove("dialog-open");
};

dialogClose.addEventListener("click", closeDialog);

dialog.addEventListener("click", (event) => {
  if (event.target === dialog) closeDialog();
});

dialog.addEventListener("close", () => {
  document.body.classList.remove("dialog-open");
});

const revealObserver = new IntersectionObserver((entries, observer) => {
  entries.forEach((entry) => {
    if (entry.isIntersecting) {
      entry.target.classList.add("visible");
      observer.unobserve(entry.target);
    }
  });
}, { threshold: 0.12 });

document.querySelectorAll(".reveal").forEach((element) => revealObserver.observe(element));
